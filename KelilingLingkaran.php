<?php
// Menghitung keliling lingkaran dengan jari-jari 15 cm
$radius = 15; // jari-jari lingkaran dalam cm
$pi = 3.14; // nilai pi

// Rumus keliling lingkaran: K = 2 * pi * r
$keliling = 2 * $pi * $radius;

// Menampilkan hasil
echo "Keliling lingkaran dengan jari-jari 15 cm adalah: " . $keliling . " cm";
?>
